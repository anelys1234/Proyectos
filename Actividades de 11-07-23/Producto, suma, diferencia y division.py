NUM1 = float(input('Ingrese un numero: '))
NUM2 = float(input('Ingrese otro numero: '))

if NUM1 > NUM2:
    PRODUCTO = NUM1 * NUM2
    print(PRODUCTO)

    SUMA = NUM1 + NUM2
    print(SUMA)
else:
    DIFERENCIA = NUM2 - NUM1
    print(DIFERENCIA)

    DIVISION = NUM2 / NUM1
    print(DIVISION)

    
